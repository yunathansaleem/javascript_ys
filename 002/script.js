//rules for naming variables
// 1. Cannot start with a number
// 2. Cannot contain spaces
// 3. Cannot use reserved keywords (like var, let, const, function, etc.)
// 4. Case sensitive
// 5. Can use letters, numbers, underscores, and dollar signs
// 6. Should be descriptive and meaningful
// 7. Cannot use special characters (like @, #, $, %, etc.)
// 8. Cannot use hyphens (-) as they are interpreted as subtraction

// Examples of valid variable names
var firstName = 'John'
let lastName = 'Doe'
const birthYear = 2000
